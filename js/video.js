// Pop UP 1
function showVideoPopup() {
    // Gantilah 'VIDEO_ID' dengan ID video YouTube yang ingin Anda tampilkan
    var videoId = 'UOrQV7r_fX8';

    var popupHtml = `
    <div class="modal fade" style="margin-top: 4%" id="videoModal" tabindex="-1" role="dialog" aria-labelledby="videoModalLabel" aria-hidden="true">
      <div class="modal-dialog modal-lg">
        <div class="modal-content border-0">
          <button type="button" class="close" onclick="closeVideoPopup()" aria-label="Close" style="position: absolute; top: -50px; right: 0px; padding-top: 15px; padding-bottom: 0px; border: none; background: white; cursor: pointer; ">
            <span aria-hidden="true" style="font-size: 30px;">&times;</span>
          </button>
            <div class="modal-body">
            <iframe width="100%" height="450" src="https://www.youtube.com/embed/${videoId}?autoplay=1" frameborder="0" allow="autoplay; encrypted-media" allowfullscreen></iframe>
             </div>
        </div>
      </div>
    </div>
  `;

    // Hapus modal lama jika ada
    $('#videoModal').remove();
    // Masukkan HTML popup ke dalam halaman
    $('body').append(popupHtml);

    // Tampilkan modal
    $('#videoModal').modal('show');

    // Tangkap peristiwa penutupan modal
    $('#videoModal').on('hidden.bs.modal', function () {
        // Hapus iframe untuk menghentikan video saat menutup modal
        $('#videoModal .modal-body').html('');
    });
}

function closeVideoPopup() {
    // Sembunyikan modal
    $('#videoModal').modal('hide');
}

